package io.SampleLogin.Controller;


import java.util.List;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.Authentication;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

import io.SampleLogin.Model.Post;
import io.SampleLogin.Model.User;
import io.SampleLogin.Provider.FacebookProvider;
import io.SampleLogin.Provider.GoogleProvider;
import io.SampleLogin.Repository.PostRepository;
import io.SampleLogin.Repository.UserRepository;

@Controller
@RequestMapping("/rest/auth")
public class ApplicationController {
	
	@Autowired
	private UserRepository userRepository;

	@Autowired
	private PostRepository postRepository;
	
	@Autowired 
	FacebookProvider facebookProvider;
	 
	 @Autowired 
	 GoogleProvider googleProvider;
	 
	 
	 @RequestMapping(value = "/facebook", method = RequestMethod.GET)
	 public String loginToFacebook(Model model) {
	     return facebookProvider.getFacebookUserData(model, new User());
	 }
	 
	 @RequestMapping(value = "/google", method = RequestMethod.GET)
	 public String loginToGoogle(Model model) {
	 return googleProvider.getGoogleUserData(model, new User());
	 }
	 


	@GetMapping("/welcome")
	public String process(Model model) {
		
		Authentication authentication = SecurityContextHolder.getContext().getAuthentication();

		model.addAttribute("CurrentUserName", authentication.getName());
		
		User user = userRepository.findByUsername(authentication.getName());
		
		List<Post> posts = postRepository.findByUserId(user.getUser_id());
		
		model.addAttribute("posts", posts);
		
		return "home";
	}
}






















































































































































































































































































































































































































